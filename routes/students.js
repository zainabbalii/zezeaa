router.get("/:id", (req, res) => {
    const id = req.params.id;
    const student = students.find((student) => student.id === id);
    if (student) {
        res.json(student);
    } else {
        res.send("student not found");
    }
    
});


router.get("/city/:city", (req, res) => {
    const cityName = req.params.city;
    const studentsInCity = students.filter((student) => student.address.city === cityName);
    if (studentsInCity.length > 0) {
        res.json(studentsInCity);
    } else {
        res.send("No students found in the specified city");
    }
});

router.get("/company/:company", (req, res) => {
    const companyName = req.params.company;
    const studentsIncompany = students.filter((student) => student.company.name === companyName);
    if (studentsIncompany.length > 0) {
        res.json(studentsIncompany);
    } else {
        res.send("No students found in the specified company");
    }
});


router.get("/street/:street", (req, res) => {
    const streetName = req.params.street;

    // استخدام filter للعثور على الطلاب الذين يعيشون في الشارع المحدد
    const studentsInStreet = students.filter((student) => student.address.street === streetName);

    if (studentsInStreet.length > 0) {
        res.json(studentsInStreet);
    } else {
        res.send("No students found in the specified street");
    }
});





